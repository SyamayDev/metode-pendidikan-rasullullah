document.addEventListener('DOMContentLoaded', () => {
  const resultsContainer = document.getElementById('results');
  const searchInput = document.getElementById('searchInput');
  const refreshBtn = document.getElementById('refreshBtn');

  function displayArticles(articlesToShow) {
    resultsContainer.innerHTML = '';
    articlesToShow.forEach(article => {
      const card = document.createElement('div');
      card.className = 'col-md-4 card';
      card.innerHTML = `
        <div class="card-body">
          <img src="${article.image}" class="card-img-top" alt="${article.title}">
          <h5 class="card-title">${article.title}</h5>
          <div class="card-text">${article.content}</div>
        </div>
      `;
      resultsContainer.appendChild(card);
    });
  }

  function filterArticles(query) {
    const filtered = articles.filter(article =>
      article.title.toLowerCase().includes(query.toLowerCase()) ||
      article.content.toLowerCase().includes(query.toLowerCase())
    );
    displayArticles(filtered);
    document.getElementById('no-more-results').style.display = filtered.length === 0 ? 'block' : 'none';
  }

  searchInput.addEventListener('input', () => {
    filterArticles(searchInput.value);
  });

  refreshBtn.addEventListener('click', () => {
    searchInput.value = '';
    displayArticles(articles);
    document.getElementById('no-more-results').style.display = 'none';
  });

  // Tampilkan semua artikel saat halaman dimuat
  displayArticles(articles);
});